package datawave.core.iterators;

import java.io.IOException;

public class IvaratorException extends IOException {

    private static final long serialVersionUID = -9088671546411939958L;

    public IvaratorException() {
        super();
    }

    public IvaratorException(String message, Throwable cause) {
        super(message, cause);
    }

    public IvaratorException(String message) {
        super(message);
    }

    public IvaratorException(Throwable cause) {
        super(cause);
    }

}
